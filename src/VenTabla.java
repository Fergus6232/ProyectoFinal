    private void ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarActionPerformed
        ResultSet rs = null;
        Connection conexion = getConexion();

        try {
            PreparedStatement ps = (PreparedStatement) conexion.prepareStatement("update productos set Fabricante='"
                    + Fabricante.getText() + "', Modelo='" + Modelo.getText() + "', Ensamblador='" + Ensamblador.getText()
                    + "', Cantidad='" + Cantidad.getText() + "' where IdRegistro='" + Id.getText() + "'");

            int result = ps.executeUpdate();

            if (result > 0) {
                JOptionPane.showMessageDialog(null, "Registro modificado de manera exitosa");
                cargarTabla();
                limpiar();

            } else {
                JOptionPane.showMessageDialog(null, "Registro no modificado");
            }

            conexion.close();

        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, "Error " + ex);
        }
    }//GEN-LAST:event_ActualizarActionPerformed

