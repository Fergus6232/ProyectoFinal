    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        String query = "";
        query = "SELECT* FROM productos WHERE IdRegistro=" + CampoBuscar.getText();
        DefaultTableModel modeloTabla = new DefaultTableModel();
        TablaComponentes.setModel(modeloTabla);

        modeloTabla.addColumn("IdRegistro");
        modeloTabla.addColumn("Fabricante");
        modeloTabla.addColumn("Modelo");
        modeloTabla.addColumn("Ensamblador");
        modeloTabla.addColumn("Cantidad");

        PreparedStatement ps;
        ResultSet rs;

        try {
            Connection con = getConexion();

            ps = (PreparedStatement) con.prepareStatement(query);
            rs = ps.executeQuery();

            while (rs.next()) {

                Id.setEnabled(false);
                Id.setText(rs.getString("IdRegistro"));
                Fabricante.setText(rs.getString("Fabricante"));
                Modelo.setText(rs.getString("Modelo"));
                Ensamblador.setText(rs.getString("Ensamblador"));
                Cantidad.setText(rs.getString("Cantidad"));
                Actualizar.setEnabled(true);
                activar();

                Object fila[] = new Object[5];
                for (int i = 0; i < 5; i++) {
                    fila[i] = rs.getObject(i + 1);
                }
                modeloTabla.addRow(fila);
            }

        } catch (SQLException e) {
            System.err.println("Error " + e);
        }

    }//GEN-LAST:event_BuscarActionPerformed

